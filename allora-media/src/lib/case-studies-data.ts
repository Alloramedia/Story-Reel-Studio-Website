/** Return a flat array of unique industry names from all case studies */
export function getCaseStudyIndustries(): string[] {
  return [
    "All",
    ...Array.from(new Set(caseStudies.map((cs) => cs.industry))),
  ];
}

/** Return a flat array of unique video types (if populated) */
export function getCaseStudyVideoTypes(): string[] {
  const types = caseStudies
    .map((cs) => cs.videoType)
    .filter((v): v is string => Boolean(v));
  if (types.length === 0) return [];
  return ["All", ...Array.from(new Set(types))];
}

/** Look up a single case study by slug */
export function getCaseStudyBySlug(slug: string): CaseStudy | undefined {
  return caseStudies.find((cs) => cs.slug === slug);
}

/** Convenience export used by the /case-studies listing page */
export const industries = getCaseStudyIndustries();
